// Helper functions
export const getAdvancedKanjiByCategory = (category: string): KanjiCharacter[] => {
  return advancedKanjiData.filter(kanji => kanji.category === category);
};

export const getAdvancedKanjiByGrade = (grade: number): KanjiCharacter[] => {
  return advancedKanjiData.filter(kanji => kanji.grade === grade);
};

export const getAdvancedKanjiByStrokeCount = (strokes: number): KanjiCharacter[] => {
  return advancedKanjiData.filter(kanji => kanji.strokes === strokes);
};

export const searchAdvancedKanji = (query: string): KanjiCharacter[] => {
  const lowercaseQuery = query.toLowerCase();
  return advancedKanjiData.filter(kanji => 
    kanji.character.includes(query) ||
    kanji.meaning.toLowerCase().includes(lowercaseQuery) ||
    kanji.onyomi.includes(query) ||
    kanji.kunyomi.includes(query)
  );
}; 