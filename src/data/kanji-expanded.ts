// Helper functions
export const getExpandedKanjiByCategory = (category: string): KanjiCharacter[] => {
  return expandedKanjiData.filter(kanji => kanji.category === category);
};

export const getExpandedKanjiByGrade = (grade: number): KanjiCharacter[] => {
  return expandedKanjiData.filter(kanji => kanji.grade === grade);
};

export const getExpandedKanjiByStrokeCount = (strokes: number): KanjiCharacter[] => {
  return expandedKanjiData.filter(kanji => kanji.strokes === strokes);
};

export const searchExpandedKanji = (query: string): KanjiCharacter[] => {
  const lowercaseQuery = query.toLowerCase();
  return expandedKanjiData.filter(kanji => 
    kanji.character.includes(query) ||
    kanji.meaning.toLowerCase().includes(lowercaseQuery) ||
    kanji.onyomi.includes(query) ||
    kanji.kunyomi.includes(query)
  );
}; 