// Helper functions
export const getFinalKanjiByCategory = (category: string): KanjiCharacter[] => {
  return finalKanjiData.filter(kanji => kanji.category === category);
};

export const getFinalKanjiByGrade = (grade: number): KanjiCharacter[] => {
  return finalKanjiData.filter(kanji => kanji.grade === grade);
};

export const getFinalKanjiByStrokeCount = (strokes: number): KanjiCharacter[] => {
  return finalKanjiData.filter(kanji => kanji.strokes === strokes);
};

export const searchFinalKanji = (query: string): KanjiCharacter[] => {
  const lowercaseQuery = query.toLowerCase();
  return finalKanjiData.filter(kanji => 
    kanji.character.includes(query) ||
    kanji.meaning.toLowerCase().includes(lowercaseQuery) ||
    kanji.onyomi.includes(query) ||
    kanji.kunyomi.includes(query)
  );
}; 