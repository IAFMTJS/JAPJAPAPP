// Helper functions
export const getMoreKanjiByCategory = (category: string): KanjiCharacter[] => {
  return moreKanjiData.filter(kanji => kanji.category === category);
};

export const getMoreKanjiByGrade = (grade: number): KanjiCharacter[] => {
  return moreKanjiData.filter(kanji => kanji.grade === grade);
};

export const getMoreKanjiByStrokeCount = (strokes: number): KanjiCharacter[] => {
  return moreKanjiData.filter(kanji => kanji.strokes === strokes);
};

export const searchMoreKanji = (query: string): KanjiCharacter[] => {
  const lowercaseQuery = query.toLowerCase();
  return moreKanjiData.filter(kanji => 
    kanji.character.includes(query) ||
    kanji.meaning.toLowerCase().includes(lowercaseQuery) ||
    kanji.onyomi.includes(query) ||
    kanji.kunyomi.includes(query)
  );
}; 